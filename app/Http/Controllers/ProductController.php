     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'name' => ['required'],
            'sku' => ['required'],
            'brand_id' => ['required'],
            'quantity' => ['required'],
            'price' => ['required'],
        ]);

        Product::create($request);

        return Redirect::route('admin/products');
    }

